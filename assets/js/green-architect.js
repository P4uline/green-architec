function getPlantRow(plant) {
    var $row = $('<div/>', { 'class':'row' });

    for (i = 0; i < 12; i++) {
        var $plant = $('<div/>', { 'class':'col-xs-1' });
        $row.append($plant.append('<img src=' + plant.season[i] +' alt="feuillage" height="30" width="30"/>'));
    }
    return $row;
}

function getTitleRow(name) {
    var $title = $('<div/>', { 'class':'col-md-12' });
    $title.append(name);

    var $row = $('<div/>', { 'class':'row' });
    $row.append($title);
    return $row;
}

function getGristackItem(name) {
    var plant = getPlantRow(plants[name]);
    var title = getTitleRow(plants[name].name);
    var $content = $('<div class="grid-stack-item-content" />').append(title).append(plant);
    return $('<div></div>').append($content);
}
